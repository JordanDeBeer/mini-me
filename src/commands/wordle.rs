#[command]
#[only_in(guild)]
#[description = "wordle spoiler"]
#[min_args(0)]
pub async fn wordle(ctx: &Context, msg: &Message, _args: Args) -> CommandResult {
    let _text = _args.rest().to_string();

    let res_text: String = format!(
        "Today's wordle is {:#?}",
        date = WORDLE_SOLUTIONS.get(&Utc::today())
    );

    msg.channel_id
        .send_message(&ctx.http, |m| {
            m.content(&res_text).allowed_mentions(|am| am.empty_parse())
        })
        .await?;

    return Ok(());
}
